char	*ft_strchr(const char *s, int c)
{
	int	i;

	i = 0;
	if (c == '\0')
		return "\0";
	while (s[i] != c && s[i])
	{
		i++;
	}
	if (s[i])
		return ((char *)(s + i));
	else
		return (0);
}
